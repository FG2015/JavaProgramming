package day17_branchingStatement_DO_WhileDoLoops;

public class LoopPractices {

    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);

        }

        System.out.println("-----------------------");

        int a=5;
        while (a<=10){
            System.out.println(a);
            a++;
        }


        System.out.println("-------------------------");
int k=1;

        do {
            System.out.println(k);
            k++;
        }while (k<=10);












    }
}
